/**
 * Demonstrates the use of the string concatenation operator and the automatic
 * conversion of an integer to a string.
 * 
 * @author Lewis & Loftus 9e
 * @author Bruce Link
 * @version 2025
 */
void main() {

    // Strings can be concatenated into one long String
    IO.println("We present the following facts for your " + "extracurricular edification:");

    IO.println();

    // A String can contain numeric digits
    IO.println("Letters in the Hawaiian alphabet: 12");

    // A numeric value can be concatenated to a String
    IO.println("Dialing code for Antarctica: " + 672);

    IO.println("Year in which Leonardo da Vinci invented " + "the parachute: " + 1515);

    IO.println("Speed of ketchup: " + 40 + " km per year");
}

/**
 * Demonstrates the use of an assignment statement to change the value stored in
 * a variable.
 * 
 * @author Lewis & Loftus 9e
 * @author Bruce Link
 * @version 2025
 */
void main(String[] args) {
    // declaration with initialization
    var sides = 7;
    IO.println("A heptagon has " + sides + " sides.");

    // assignment statement
    sides = 10;
    IO.println("A decagon has " + sides + " sides.");

    sides = 12;
    IO.println("A dodecagon has " + sides + " sides.");
}
